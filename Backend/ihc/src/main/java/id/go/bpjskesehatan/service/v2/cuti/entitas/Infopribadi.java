public class Infopribadi {
	private String npp;
	private String hp;
	private String alamat;
	private int kodestatuskaryawan;
	
	public String getNpp() {
		return npp;
	}
	public void setNpp(String npp) {
		this.npp = npp;
	}
	public String getHp() {
		return hp;
	}
	public void setHp(String hp) {
		this.hp = hp;
	}
	public String getAlamat() {
		return alamat;
	}
	public void setAlamat(String alamat) {
		this.alamat = alamat;
	}
	public int getKodestatuskaryawan() {
		return kodestatuskaryawan;
	}
	public void setKodestatuskaryawan(int kodestatuskaryawan) {
		this.kodestatuskaryawan = kodestatuskaryawan;
	}
	
	
}