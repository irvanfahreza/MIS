package id.go.bpjskesehatan.service.v2.karyawan.entitas;

public class InfoKontakDarurat {
	private Integer kode;
	private Integer npp;
	private String kodehubungankeluarga;
	private String nama;
	private Integer kodepekerjaan;
	private String telprumah;
	private String fax;
	private String hp;
	private String hp2;
	private String alamat;
	public Integer getKode() {
		return kode;
	}
	public void setKode(Integer kode) {
		this.kode = kode;
	}
	public Integer getNpp() {
		return npp;
	}
	public void setNpp(Integer npp) {
		this.npp = npp;
	}
	public String getKodehubungankeluarga() {
		return kodehubungankeluarga;
	}
	public void setKodehubungankeluarga(String kodehubungankeluarga) {
		this.kodehubungankeluarga = kodehubungankeluarga;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public Integer getKodepekerjaan() {
		return kodepekerjaan;
	}
	public void setKodepekerjaan(Integer kodepekerjaan) {
		this.kodepekerjaan = kodepekerjaan;
	}
	public String getTelprumah() {
		return telprumah;
	}
	public void setTelprumah(String telprumah) {
		this.telprumah = telprumah;
	}
	public String getFax() {
		return fax;
	}
	public void setFax(String fax) {
		this.fax = fax;
	}
	public String getHp() {
		return hp;
	}
	public void setHp(String hp) {
		this.hp = hp;
	}
	public String getHp2() {
		return hp2;
	}
	public void setHp2(String hp2) {
		this.hp2 = hp2;
	}
	public String getAlamat() {
		return alamat;
	}
	public void setAlamat(String alamat) {
		this.alamat = alamat;
	}
	
}
